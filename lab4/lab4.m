y = [93; 97; 96; 87; 90; 84; 87; 87; 79; 84; 75; 76; 80; 69; 75; 64; 67; 59; 62; 50;
     52; 50; 54; 55; 56; 51; 62; 65; 57; 77; 74; 69; 83; 82; 76; 77; 86; 80; 91; 96];

X = x(1):0.1:x(end);
Y1 = polyval(polynom(x, y, 1), X);
Y2 = polyval(polynom(x, y, 2), X);
