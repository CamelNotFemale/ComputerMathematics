f = func(x, y);
Y4 = f(1)*exp(X) + f(2)*log(X) + f(3);

plot(x,y,'*');
hold on;
grid on;
xlabel('x');
ylabel('y');
title('Графики');

plot(X,Y1,'Color','r');
plot(X,Y2,'Color','g');
plot(X,Y3,'Color','b');
